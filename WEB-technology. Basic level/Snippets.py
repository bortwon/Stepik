# Тег <p> представляет собой абзац текста. На странице может быть любое количество абзацев. Абзац всегда начинается с
# новой строки.
# --------------------------------------------------


# --------------------------------------------------
# Теги <h1> - <h6> представляют собой заголовки с 1-6 уровни.
#
# Обычно на странице присутствует один заголовок первого уровня h1. Крайне редко используются заголовки с 3-6 уровни.

# Также не рекомендуется пропускать заголовки. У вас не может быть заголовок h1, а затем сразу h3. Должна соблюдаться
# вложенность.
